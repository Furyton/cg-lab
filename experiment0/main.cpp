int main() {

    Window window;

    if (window.init_window(800, 600)) {
        return -1;
    }

    Shader shader("./experiment0/shaders/shader.vs", "./experiment0/shaders/shader.fs");

    Attribute attr = Attribute();

    attr.set_vertices(sizeof(vertices), vertices);
    // attr.set_indices(sizeof(indices), indices);
    attr.add_attr(0, 3);
    attr.add_attr(1, 2);

    attr.build(GL_STATIC_DRAW);

    while(!window.should_close()) {
        window.process_input();

        glClearColor(0.2f, 0.3f, 0.3f, 1.0f);
        glClear(GL_COLOR_BUFFER_BIT | GL_DEPTH_BUFFER_BIT);

        shader.use();
        
        attr.activate();

        float t = (float)glfwGetTime();

        glm::mat4 view = glm::mat4(1.0f);
        view = glm::translate(view, glm::vec3(0.0f, 0.0f, -3.0f));
        
        shader.setVec3("offset_color", glm::vec3(t, t, t));
        glm::mat4 model = glm::mat4(1.0f);
        glm::mat4 projection = glm::perspective(glm::radians(45.0f), 800.0f / 600.0f, 0.1f, 100.0f);
        // model = glm::translate(model, cubePositions[i]);
        float angle = 20.0f; 
        model = glm::rotate(model, (float)glfwGetTime() * glm::radians(angle), glm::vec3(1.0f, 0.3f, 0.5f));

        shader.setMat4("model", model);
        shader.setMat4("view", view);
        shader.setMat4("projection", projection);

        glDrawArrays(GL_TRIANGLES, 0, 36);

        attr.deactivate();

        window.swap_buffers();
        window.polling_events();
    }

    attr.destroy();

    window.close();

    return 0;
}